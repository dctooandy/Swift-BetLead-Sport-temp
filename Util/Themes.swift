    static func getOddsStr(old:Double ,new:Double , normalColor:UIColor = .white ,darkMode:Bool = false) -> NSAttributedString {
        if new == old {
            return NSAttributedString(string: "@\(new)", attributes: [NSAttributedString.Key.foregroundColor:normalColor])
        } else if new > old {
            return NSAttributedString(string: "@\(new)↑", attributes: [NSAttributedString.Key.foregroundColor: Themes.priceRise])
        } else {
            return NSAttributedString(string: "@\(new)↓", attributes: [NSAttributedString.Key.foregroundColor:darkMode ? Themes.darkPriceFall : Themes.priceFall])
        }
    }
    static func getOddsStrWithoutSymbol(old:Double ,new:Double , normalColor:UIColor = .white ,darkMode:Bool = false) -> NSAttributedString {
        if new == old {
            return NSAttributedString(string: "\(new)", attributes: [NSAttributedString.Key.foregroundColor:normalColor])
        } else if new > old {
            return NSAttributedString(string: "\(new)", attributes: [NSAttributedString.Key.foregroundColor:Themes.priceRise])
        } else {
            return NSAttributedString(string: "\(new)", attributes: [NSAttributedString.Key.foregroundColor:darkMode ? Themes.darkPriceFall : Themes.priceFall])
        }
    }
    static func getOddsStrColor(old:Double ,new:Double , normalColor:UIColor = .white ,darkMode:Bool = false) -> UIColor {
        if new == old {
            return normalColor
        } else if new > old {
            return Themes.priceRise
        } else {
            return darkMode ? Themes.darkPriceFall : Themes.priceFall
        }
    }
    
    
